package com.backGroundManager.dto;

public class CnameDto {

    int cid;
    String cname;

    @Override
    public String toString() {
        return "CnameDto{" +
                "cid=" + cid +
                ", cname='" + cname + '\'' +
                '}';
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }
}
